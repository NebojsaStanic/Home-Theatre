
public class DvdPlayer {

	public void on() {
		System.out.println("DvdPlayer is on");
		
	}

	public void play(String movie) {
		System.out.println("DvdPlayer is playng movie " + movie);
		
	}

	public void stop() {
		System.out.println("DvdPlayer is stoping");
		
	}

	public void eject() {
		System.out.println("DvdPlayer is ejecting dvd");
		
	}

	public void off() {
		System.out.println("DvdPlayer is off");
		
	}

}
